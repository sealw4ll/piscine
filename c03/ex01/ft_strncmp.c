int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				y;

	i = 0;
	while ((s1[i] != '\0' || s2[i] != '\0' || i < n) && (n != 0))
	{
		y = s1[i] - s2[i];
		if (y == 0)
			i++;
		else
			return (y);
	}
	return (0);
}
